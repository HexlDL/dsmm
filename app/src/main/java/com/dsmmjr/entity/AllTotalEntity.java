public class AllTotalEntity {

    /**
     * code : 1
     * msg : Success
     * data : {"borrow":{"invest_money":"88500"}}
     */

    private int code;
    private String msg;
    private DataBean data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public DataBean getData() {
        return data;
    }

    public void setData(DataBean data) {
        this.data = data;
    }

    public static class DataBean {
        /**
         * borrow : {"invest_money":"88500"}
         */

        private BorrowBean borrow;

        public BorrowBean getBorrow() {
            return borrow;
        }

        public void setBorrow(BorrowBean borrow) {
            this.borrow = borrow;
        }

        public static class BorrowBean {
            /**
             * invest_money : 88500
             */

            private String invest_money;

            public String getInvest_money() {
                return invest_money;
            }

            public void setInvest_money(String invest_money) {
                this.invest_money = invest_money;
            }
        }
    }
}
