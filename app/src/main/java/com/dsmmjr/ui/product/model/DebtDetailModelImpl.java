public class DebtDetailModelImpl implements IDebtDetailModel {

    @Override
    public void loadBidDetail(String borrow_id, String invest_id, final ICallBackListener<DebtDetailEntity> listener) {

        Map<String, String> params = new HashMap<>();

        params.put("borrow_id", borrow_id);
        params.put("invest_id", invest_id);

        OkHttpUtils.ResultCallback<String> callback = new OkHttpUtils.ResultCallback<String>() {
            @Override
            public void onError(Request request, Exception e) {
                listener.onFailed(request, e);
            }

            @Override
            public void onResponse(String response) {
                listener.onSuccessed(JsonUtils.deserialize(response, DebtDetailEntity.class));
            }
        };
        OkHttpUtils.postAsyn(BASE_HTTP + "/debt/getDebtDetail", callback, params);
    }
}
