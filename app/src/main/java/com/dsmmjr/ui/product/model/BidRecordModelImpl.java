public class BidRecordModelImpl implements IBidRecordModel {

    @Override
    public void getInvestrecord(String id, int p, int pagesize, final ICallBackListener<BidRecordEntity> listener) {

        Map<String, String> params = new HashMap<>();

        params.put("id", id);
        params.put("p", String.valueOf(p));
        params.put("pagesize", String.valueOf(pagesize));

        OkHttpUtils.ResultCallback<String> callback = new OkHttpUtils.ResultCallback<String>() {
            @Override
            public void onError(Request request, Exception e) {
                listener.onFailed(request, e);
            }

            @Override
            public void onResponse(String response) {
                listener.onSuccessed(JsonUtils.deserialize(response, BidRecordEntity.class));
            }
        };
        OkHttpUtils.postAsyn(BASE_HTTP + "/borrow/getInvestrecord", callback, params);
    }
}
