public class InvestDetailModelImpl implements IInvestDetailModel {

    @Override
    public void loadInvest(String token, String id, final ICallBackListener<InvestDetailEntity> listener) {
        Map<String, String> params = new HashMap<>();
        params.put("token",token);
        params.put("id",id);

        OkHttpUtils.ResultCallback<String> callback = new OkHttpUtils.ResultCallback<String>() {
            @Override
            public void onError(Request request, Exception e) {
                listener.onFailed(request, e);
            }

            @Override
            public void onResponse(String response) {
                listener.onSuccessed(JsonUtils.deserialize(response, InvestDetailEntity.class));
            }
        };
        OkHttpUtils.postAsyn(BASE_HTTP + "/borrow/getBorrowDetail", callback, params);
    }
}
