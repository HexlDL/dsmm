public class RegisterVerifyModelImpl implements IRegisterVerifyModel {
    @Override
    public void validRegCode(String phone, String smscode, final ICallBackListener<RegisterEntity> listener) {
        Map<String, String> params = new HashMap<>();
        params.put("phone", phone);
        params.put("smscode", smscode);
        OkHttpUtils.ResultCallback<String> callback = new OkHttpUtils.ResultCallback<String>() {
            @Override
            public void onError(Request request, Exception e) {
                listener.onFailed(request, e);
            }

            @Override
            public void onResponse(String response) {
                listener.onSuccessed(JsonUtils.deserialize(response, RegisterEntity.class));
            }
        };
        OkHttpUtils.postAsyn(BASE_HTTP + "/pub/valid_regcode", callback, params);
    }
}
