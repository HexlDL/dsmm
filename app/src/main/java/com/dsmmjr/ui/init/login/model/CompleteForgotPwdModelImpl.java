public class CompleteForgotPwdModelImpl implements ICompleteForgotPwdModel {

    @Override
    public void forgotPassword(String mobile, final ICallBackListener<ResponseInfoEntity> listener) {

        Map<String, String> params = new HashMap<>();

        params.put("mobile", mobile);

        OkHttpUtils.ResultCallback<String> callback = new OkHttpUtils.ResultCallback<String>() {
            @Override
            public void onError(Request request, Exception e) {
                listener.onFailed(request, e);
            }

            @Override
            public void onResponse(String response) {
                listener.onSuccessed(JsonUtils.deserialize(response, ResponseInfoEntity.class));
            }
        };
        OkHttpUtils.postAsyn(BASE_HTTP + "/pub/forgotPassword", callback, params);
    }

    @Override
    public void resetPassword(String mobile, String new_password, String code, final ICallBackListener<ResponseInfoEntity> listener) {

        Map<String, String> params = new HashMap<>();

        params.put("mobile", mobile);
        params.put("newpass", new_password);
        params.put("smscode", code);

        OkHttpUtils.ResultCallback<String> callback = new OkHttpUtils.ResultCallback<String>() {
            @Override
            public void onError(Request request, Exception e) {
                listener.onFailed(request, e);
            }

            @Override
            public void onResponse(String response) {
                listener.onSuccessed(JsonUtils.deserialize(response, ResponseInfoEntity.class));
            }
        };
        OkHttpUtils.postAsyn(BASE_HTTP + "/pub/resetPassword", callback, params);
    }
}
