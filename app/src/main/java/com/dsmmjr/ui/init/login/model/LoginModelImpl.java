public class LoginModelImpl implements ILoginModel {

    @Override
    public void login(String username, String password, final ICallBackListener<LoginEntity> listener)
            throws JsonSyntaxException {

        Map<String, String> params = new HashMap<>();
        params.put("username", username);
        params.put("password", password);

        OkHttpUtils.ResultCallback<String> callback = new OkHttpUtils.ResultCallback<String>() {
            @Override
            public void onError(Request request, Exception e) {
                listener.onFailed(request, e);
            }

            @Override
            public void onResponse(String response) {
                listener.onSuccessed(JsonUtils.deserialize(response, LoginEntity.class));
            }
        };
        OkHttpUtils.postAsyn(BASE_HTTP + "/pub/login", callback, params);
    }


}
