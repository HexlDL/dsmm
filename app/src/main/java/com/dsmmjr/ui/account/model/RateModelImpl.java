public class RateModelImpl implements IRateModel {
    @Override
    public void getRate(String token, int defualtPage, int defualtPageCount, final ICallBackListener<RateEntity> listener) {

        Map<String, String> params = new HashMap<>();

        params.put("token", token);
        params.put("p", String.valueOf(defualtPage));
        params.put("pagesize", String.valueOf(defualtPageCount));

        OkHttpUtils.ResultCallback<String> callback = new OkHttpUtils.ResultCallback<String>() {
            @Override
            public void onError(Request request, Exception e) {
                listener.onFailed(request, e);
            }

            @Override
            public void onResponse(String response) {
                listener.onSuccessed(JsonUtils.deserialize(response, RateEntity.class));
            }
        };
        OkHttpUtils.postAsyn(BASE_HTTP + "/User/getCoupon", callback, params);
    }

    @Override
    public void verifyCoupon(String token, String code, final ICallBackListener<ResponseInfoEntity> listener) {
        Map<String, String> params = new HashMap<>();
        params.put("token", token);
        params.put("code", code);

        OkHttpUtils.ResultCallback<String> callback = new OkHttpUtils.ResultCallback<String>() {
            @Override
            public void onError(Request request, Exception e) {
                listener.onFailed(request, e);
            }

            @Override
            public void onResponse(String response) {
                listener.onSuccessed(JsonUtils.deserialize(response, ResponseInfoEntity.class));
            }
        };
        OkHttpUtils.postAsyn(BASE_HTTP + "/User/verifyCoupon", callback, params);
    }
}
