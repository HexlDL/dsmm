public class UnFyAccountModelImpl implements IUnFyAccountModel {

    @Override
    public void doreg(String token, String cust_nm, String idcard,
                      final ICallBackListener<UnOpenAccountEntity> listener) {

        Map<String, String> params = new HashMap<>();

        params.put("token", token);
        params.put("cust_nm", cust_nm);
        params.put("idcard", idcard);

        OkHttpUtils.ResultCallback<String> callback = new OkHttpUtils.ResultCallback<String>() {
            @Override
            public void onError(Request request, Exception e) {
                listener.onFailed(request, e);
            }

            @Override
            public void onResponse(String response) {
                listener.onSuccessed(JsonUtils.deserialize(response, UnOpenAccountEntity.class));
            }
        };
        OkHttpUtils.postAsyn(DsmmConfig.BASE_HTTP + "/fuiou/doreg", callback, params);
    }
}