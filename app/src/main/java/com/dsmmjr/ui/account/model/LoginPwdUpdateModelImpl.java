public class LoginPwdUpdateModelImpl implements ILoginPwdUpdateModel {

    @Override
    public void editpass(String token, String oldpass, String newpass,
                         final ICallBackListener<ResponseInfoEntity> listener) {
        Map<String, String> params = new HashMap<>();

        params.put("token", token);
        params.put("oldpass", oldpass);
        params.put("newpass", newpass);

        OkHttpUtils.ResultCallback<String> callback = new OkHttpUtils.ResultCallback<String>() {
            @Override
            public void onError(Request request, Exception e) {
                listener.onFailed(request, e);
            }

            @Override
            public void onResponse(String response) {
                listener.onSuccessed(JsonUtils.deserialize(response, ResponseInfoEntity.class));
            }
        };
        OkHttpUtils.postAsyn(BASE_HTTP + "/User/editpass", callback, params);
    }
}
