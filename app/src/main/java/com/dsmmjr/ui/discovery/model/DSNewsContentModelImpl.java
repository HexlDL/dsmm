public class DSNewsContentModelImpl implements IDSNewsContentModel{

    @Override
    public void detail(String id, final ICallBackListener<DSNewsContentEntity> listener) {
        Map<String, String> params = new HashMap<>();

        params.put("id", id);

        OkHttpUtils.ResultCallback<String> callback = new OkHttpUtils.ResultCallback<String>() {
            @Override
            public void onError(Request request, Exception e) {
                listener.onFailed(request, e);
            }

            @Override
            public void onResponse(String response) {
                listener.onSuccessed(JsonUtils.deserialize(response, DSNewsContentEntity.class));
            }
        };

        OkHttpUtils.postAsyn(DsmmConfig.BASE_HTTP + "/news/detail", callback, params);
    }
}
